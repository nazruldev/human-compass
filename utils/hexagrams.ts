export type HexagramSymbol = (typeof HEXAGRAMS)[number]["symbol"];
export type HexagramLine = 0 | 1;

/** Render single line: 1 = yang (solid), 0 = yin (broken) */
export function renderLine(v: HexagramLine): string {
  return v === 1 ? "──────" : "──  ──";
}

/** Render hexagram symbol as array of line strings (bottom to top) */
export function renderHexagram(symbol: readonly number[]): string[] {
  return [...symbol].reverse().map((v) => renderLine(v as HexagramLine));
}

/** Get hexagram by id (1-64) */
export function getHexagramById(id: number) {
  return HEXAGRAMS.find((h) => h.id === id) ?? HEXAGRAMS[0];
}
